function applyI18n(lang) {
  const dict = I18N[lang] || I18N.pt;

  // Elementos com texto simples
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.innerText = dict[key];
  });

  // Elementos com HTML interno
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  // Atributos (alt, title, etc.)
  document.querySelectorAll("[data-i18n-attr-alt]").forEach((el) => {
    const key = el.getAttribute("data-i18n-attr-alt");
    if (dict[key] !== undefined) el.setAttribute("alt", dict[key]);
  });

  // <html lang="...">
  document.documentElement.setAttribute("lang", lang === "en" ? "en" : "pt-BR");

  // Botão
  const btn = document.getElementById("translate-toggle");
  if (btn) {
    if (lang === "en") {
      btn.textContent = "PT";
      btn.setAttribute("aria-label", "Voltar para português");
      btn.title = "PT / EN";
    } else {
      btn.textContent = "EN";
      btn.setAttribute("aria-label", "Translate to English");
      btn.title = "EN / PT";
    }
  }
}

(function initI18n() {
  const saved = localStorage.getItem("lang") || "pt";
  applyI18n(saved);

  const btn = document.getElementById("translate-toggle");
  if (btn) {
    btn.addEventListener("click", () => {
      const current = document.documentElement.getAttribute("lang") === "en" ? "en" : "pt";
      const next = current === "en" ? "pt" : "en";
      localStorage.setItem("lang", next);
      applyI18n(next);
    });
  }

  // Atualiza ano do copyright
  const yearEl = document.getElementById("year");
  if (yearEl) yearEl.textContent = new Date().getFullYear();
})();
